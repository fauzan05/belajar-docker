# membuat semua container yang ada di file docker-compose.yaml
docker compose create

# menjalankan semua container yang ada di file docker-compose.yaml
docker compose start

# melihat semua container yang berjalan yang ada di file docker-compose.yaml
docker compose ps

# menghentikan semua container yang ada di file docker-compose.yaml
docker compose stop

# menghapus semua contianer yang ada di file docker-compose.yaml 
docker compose down

# menampilkan informasi nama project sesuai dengan folder dimana docker-compose.yaml dijalankan
docker compose ls 